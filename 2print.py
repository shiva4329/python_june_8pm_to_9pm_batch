# normal print

print(10)

# display 10 using f'' print

print(f'10') # displaying 10 as string
print(f'{10}') # displaying 10 as int(using place holder {})


# display 10 using .format() print

print('10'.format())# displaying 10 as string

print('{0}'.format(10)) # displaying 10 as int(using place holder {})

print('{0}'.format(1,2,3)) # displaying 10 as int(using place holder {})

print('{1}'.format(1,2,3)) # displaying 10 as int(using place holder {})
