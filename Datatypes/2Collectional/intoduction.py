# collectional datatypes = sequence of values

# types = 4 types
            # List----> []
            # tuple -----> ()
            # set -------> {}
            # Dictionary ------< {key:value}


# List

# list can be defined using [] braces
# list is a mutable(changeable)
# list allows duplicate values
# list allows homogeneous and heterogeneous values
# list follows index
# list function = list()



# Tuple

# Tuple can be defined using () braces
# Tuple is a im-mutable(not changeable)
# Tuple allows duplicate values
# Tuple allows homogeneous and heterogeneous values
# Tuple follows index
# Tuple function = tuple()



# set

# Set can be defined using {} braces
# Set is a mutable(changeable)
# Set does not allows duplicate values
# Set allows homogeneous and heterogeneous values
# Set dosent follows index
# Set function = set()


# Dictionary

# Dictionary can be defined using {key:value} pair
# Dictionary is a mutable(changeable)
# Dictionary does not allow duplicate keys but allows duplicate values
# Dictionary allows homogeneous and heterogeneous values
# Dictionary dosent follows index
# Dictionary function = dict()

