# Fundamental :

#   datatypes       python fn       example
#-------------------------------------------------
#    Integer        int()           0,1,2,3,4
#    string         str()           'a','b',"hello" ---> in python string values should define in quotes(single quote('')/double quotes(""))
#    float          float()         1.1, 10.02, 3.5 ----> numbers which are with decimal points are called float datatype
#    boolen         bool()          True, False
#    Complex        complex()       10+11j, 1+2j -----> complex numbers are combination of real and imaginary values(a+bj)s



